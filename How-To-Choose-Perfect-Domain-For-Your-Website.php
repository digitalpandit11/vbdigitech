                     <div class="entry-content">
                        <p class="jt">When you're setting up a new business, one of the first things on your to-do list is choosing and buying your website address aka your domain name. There are lots of competing advice and factors to play out there, some of them are great for SEO but terrible from a branding perspective and vice versa. Not to mention that the market's pretty saturated and a lot of the best names are already being taken so, given all that choosing the perfect domain can be a bit of a juggling act for sure.</p>
                          
                        <p class="jt">I'm going to show you a practical checklist of; what to go for, what to avoid and how to prioritize all these factors to make your job of choosing that perfect domain name easy. And how can you choose that domain name that your customers can remember type in without any trouble.</p>


                          <h2 style=" font-size: 18px; font-weight: bold;">Short and Memorable </h2>

                          <div class="post-media post-image image-angle">
                             <img  src="images/blog/doman1.png" class="img-responsive" alt="">
                          </div>
                         
                          <p class="jt">This is pretty obvious and common advice suggested by everyone. The longer or more complicated your domain name is, there is more chances of people screwing it up when they attempt to share it and certainly when they type it in. </p>
                          <p class="jt">So, so now is not the time for long tail keyword phrases. Keep it as short as you possibly can, without making it too short and the hurting your branding or ranking potential.</p>


                          <h2 style=" font-size: 18px; font-weight: bold;">.COM or .ANYTHING</h2>

                          <div class="post-media post-image image-angle">
                             <img  src="images/blog/doman2.png" class="img-responsive" alt="">
                          </div>

                         
                          
                          <p class="jt">Lots of domain names you might want are probably already taken at least in the .com version. So people start thinking “hey that's okay i can just get a .net or .luxury which also is actually a real thing you can get and in fact a new industry has pretty much popped up this past decade of all these new, what we call top level domain extensions. I’ve even been hit up to advertise a few of them over the years some of them actually sound kind of trendy and cool and some are certainly specific like .beer which seems like it'd be a great fit for a brewery or .George which seems like a perfect fit for a Costanzo but, we don't recommend them for one very simple reason, i.e. people have a built-in association with .com they think of websites as .com to the point. Where if you told them to go to my website mikesdogtraining.net most people would remember that as mikesdogtraining.com </p>
                          <p class="jt">So if your .com isn't available rather than just calling .net your plan B, I’d actually make that more of a plan C behind tweaking the name of the .coms lightly if you possibly can so something like getmikesdogtraining.com that makes it a bit longer but that's something you'd want to weigh out. But, I think adding that one short  word to the beginning is worth sticking with .com and it makes the name a little more actionable and fun in my opinion and if you really can't find anything acceptable as a .com my second choice would be a .net or .co</p>


                          <h2 style=" font-size: 18px; font-weight: bold;">BRANDED OR KEYWORD DRIVEN</h2>

                          <div class="post-media post-image image-angle">
                             <img  src="images/blog/doman3.png" class="img-responsive" alt="">
                          </div>

                          
                          <p class="jt">Now we get to the biggest debate over domain names, should you go for something classy and branded or for something more keyword driven?</p>
                          <p class="jt">So, there are definite pros and cons for each of these. Let's just talk about it first off when I say a branded domain I mean something like ultaclinic.com and a keyword driven version of that might be punedermatologist.com with the pure branded (ultaclinic.com) domain it reads cleanly it's memorable and it feels like a real brand.</p>
                          <p class="jt">Those are all pluses for punedermatologist.com it closely aligns with what people might actually search in Google, which making it much more likely to rank for that term and in this case it also happens to be somewhat memorable, but it doesn't really sound like much of a brand does it you know this can be slightly or significantly damaging to your click-through rate depending on how egregious it is.</p>
                          <p class="jt">For instance how likely would you be to click through to bestplumbersinpune.com Verses allclear.com,   you know one feels like a legit company like its branded (allclear.com) and one feels like you're going to click through to a really slimy spammy page (bestplumbersinpune.com).</p>
                          <p class="jt">But choosing something purely branded can really hold you back in terms of ranking and searches.</p>


                          <h2 style=" font-size: 18px; font-weight: bold;">WHAT’S SOLUTION FOR THIS </h2>
                          <div class="post-media post-image image-angle">
                             <img  src="images/blog/doman4.png" class="img-responsive" alt="">
                          </div>
                          
                          <p class="jt">Even though some people say keyword reach domain is getting to be less and less of a ranking factor, but for now it's still a thing.</p>
                          <p class="jt">So, here's what I actually recommend: not an exact match domain like bestplumbersinpune.com but a partial match domain. Something that combines your business name and your top keyword phrase. Let’s suppose your business name is ‘all clear plumbing’ you might want to go for something like allclearplumbing.com or possibly something like acplumbingorlando.com, you know both options are still branded it's just a matter of how aggressive you feel. </p>

