                         <p class="jt">Create documents relating to your business or your niche. The documents should have quality content and should be in either pdf or ppt formats. Submit these documents in the document sharing websites. </p>
                         
                         <p class="jt" style=" font-size: 15px; font-weight: bold;">Top Document Sharing Websites: </p>
