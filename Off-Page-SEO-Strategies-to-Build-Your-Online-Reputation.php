                        <h2  style=" font-size: 18px;">4. Article Submission Directory</h2>
                         <p class="jt">Submit your articles in a high PR article submission directory. You can also give links to your website. Make sure your content is of high quality. Choose an effective directory and select a proper category or your business niche category and give a good title to your content.</p>
                         
                         <p class="jt" style=" font-size: 15px; font-weight: bold;">Top Article Submission sites: </p>
